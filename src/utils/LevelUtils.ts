    static levelToDust(level: number): number {
        if (level <= 100) {
            return LevelUtils.LevelGates[level - 1];
        } else {
            return (Math.pow(level, 3.3) * 1.3) - 4500000
        }
    }

    static dustToLevel(dust: number): number {
        if (dust <= 750000) {
            return LevelUtils.LevelGates.indexOf(LevelUtils.LevelGates.find(l => l > dust) ?? 0) + 1;
        } else {
            return Math.pow((dust + 4500000) / 1.3, 1 / 3.3)
        }
    }
